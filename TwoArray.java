import java.util.*;

public class TwoArray{
   public static void main(String[] args){
      int [] val = {13, -4, 82, 17};
      
      
      System.out.println("Original Array: " + val[0] + val[1] + val[2] + val[3]);
      int [] twice = {17,82,-4,13};
      System.out.println("Reversed array: " + twice[0] + twice [1] + twice [2] + twice [3]);
   } 
}
