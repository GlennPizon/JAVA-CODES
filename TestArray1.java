public class TestArray1{

   public static void main(String[] args){
   
      int[] x = new int[5];
   
      int counter;
   
      x[0] = 320;
   
      x[1] = 5;
   
      x[2] = 33;
   
      x[3] = 4;
   
      x[4] = 2000;
   
   
   
      counter = 4;
   
   
   
      while (counter >= 0){
      
         System.out.println(x[counter]);
      
         counter--;
      
      }
     
      
   
   }

}



